from __future__ import annotations

from collections import Counter
from datetime import datetime
from pathlib import Path
from typing import Any


def write_packing_report(result_data: dict[str, Any], path: Path) -> Path:
    Workbook, Alignment, Border, Font, PatternFill, Side = _load_openpyxl()

    workbook = Workbook()
    summary_sheet = workbook.active
    summary_sheet.title = "Упаковочный лист"
    manual_sheet = workbook.create_sheet("Ручное заполнение", 1)
    details_sheet = workbook.create_sheet("Отправочные места")

    placed_rows = _placed_rows(result_data)
    unplaced_rows = _unplaced_rows(result_data)

    _build_summary_sheet(
        sheet=summary_sheet,
        placed_rows=placed_rows,
        unplaced_rows=unplaced_rows,
        result_data=result_data,
        alignment_cls=Alignment,
        border_cls=Border,
        font_cls=Font,
        fill_cls=PatternFill,
        side_cls=Side,
    )
    _build_manual_sheet(
        sheet=manual_sheet,
        placed_rows=placed_rows,
        result_data=result_data,
        alignment_cls=Alignment,
        border_cls=Border,
        font_cls=Font,
        fill_cls=PatternFill,
        side_cls=Side,
    )
    _build_details_sheet(
        sheet=details_sheet,
        placed_rows=placed_rows,
        result_data=result_data,
        alignment_cls=Alignment,
        border_cls=Border,
        font_cls=Font,
        fill_cls=PatternFill,
        side_cls=Side,
    )

    workbook.properties.creator = "packing-mvp"
    workbook.properties.title = "Packing Report"
    workbook.properties.description = "Generated by packing-mvp."

    output_path = Path(path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    workbook.save(output_path)
    return output_path


def _build_summary_sheet(
    *,
    sheet: Any,
    placed_rows: list[dict[str, Any]],
    unplaced_rows: list[dict[str, Any]],
    result_data: dict[str, Any],
    alignment_cls: Any,
    border_cls: Any,
    font_cls: Any,
    fill_cls: Any,
    side_cls: Any,
) -> None:
    styles = _styles(
        alignment_cls=alignment_cls,
        border_cls=border_cls,
        font_cls=font_cls,
        fill_cls=fill_cls,
        side_cls=side_cls,
    )
    truck = result_data.get("truck") or {}
    used = result_data.get("used_extents_mm") or {}
    status_text = "Успешно" if result_data.get("success") else "Не все места размещены"

    sheet.merge_cells("A1:G1")
    sheet["A1"] = "Упаковочный лист"
    sheet["A1"].font = styles["title_font"]
    sheet["A1"].alignment = styles["title_alignment"]

    sheet.merge_cells("A2:G2")
    sheet["A2"] = "Шапка отчёта изменяется: при необходимости реквизиты заполняются вручную."
    sheet["A2"].font = styles["note_font"]
    sheet["A2"].alignment = styles["wrap_alignment"]

    info_rows = (
        ("Дата формирования", datetime.now().strftime("%Y-%m-%d %H:%M")),
        ("Статус", status_text),
        ("Размещено мест", int(result_data.get("packed_count") or 0)),
        ("Неразмещено мест", int(result_data.get("unpacked_count") or 0)),
        ("Кузов (мм)", _truck_dims_text(truck)),
        ("Использовано (мм)", _used_dims_text(used)),
    )
    for row_index, (label, value) in enumerate(info_rows, start=4):
        sheet[f"A{row_index}"] = label
        sheet[f"A{row_index}"].font = styles["label_font"]
        sheet[f"B{row_index}"] = value
        sheet[f"B{row_index}"].alignment = styles["wrap_alignment"]

    table_row = 11
    headers = (
        "№ места",
        "Наименование",
        "Тип",
        "Габариты места (мм)",
        "Положение в кузове (мм)",
        "Поворот",
        "Источник",
    )
    _write_table_header(sheet=sheet, row_index=table_row, headers=headers, styles=styles)

    next_row = table_row + 1
    if placed_rows:
        for row in placed_rows:
            values = (
                row["place_no"],
                row["name"],
                row["source_kind_label"],
                row["dims_text"],
                row["position_text"],
                row["rotation"],
                row["source_label"],
            )
            _write_row(sheet=sheet, row_index=next_row, values=values, styles=styles)
            next_row += 1
    else:
        sheet.merge_cells(start_row=next_row, start_column=1, end_row=next_row, end_column=7)
        sheet.cell(next_row, 1).value = "Размещённые места отсутствуют."
        sheet.cell(next_row, 1).alignment = styles["wrap_alignment"]
        sheet.cell(next_row, 1).border = styles["cell_border"]
        next_row += 1

    if unplaced_rows:
        next_row += 2
        sheet[f"A{next_row}"] = "Неразмещённые типы"
        sheet[f"A{next_row}"].font = styles["section_font"]
        next_row += 1
        _write_table_header(
            sheet=sheet,
            row_index=next_row,
            headers=("Наименование", "Количество", "Источник"),
            styles=styles,
        )
        next_row += 1
        for row in unplaced_rows:
            _write_row(
                sheet=sheet,
                row_index=next_row,
                values=(row["name"], row["quantity"], row["source_label"]),
                styles=styles,
            )
            next_row += 1

    sheet.freeze_panes = f"A{table_row + 1}"
    sheet.auto_filter.ref = f"A{table_row}:G{max(table_row + 1, table_row + len(placed_rows))}"
    _apply_sheet_layout(
        sheet=sheet,
        widths={
            "A": 12,
            "B": 34,
            "C": 14,
            "D": 20,
            "E": 26,
            "F": 14,
            "G": 48,
        },
    )



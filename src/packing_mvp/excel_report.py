def _write_table_header(*, sheet: Any, row_index: int, headers: tuple[str, ...], styles: dict[str, Any]) -> None:
    for column_index, header in enumerate(headers, start=1):
        cell = sheet.cell(row_index, column_index)
        cell.value = header
        cell.font = styles["header_font"]
        cell.fill = styles["header_fill"]
        cell.border = styles["cell_border"]
        cell.alignment = styles["header_alignment"]


def _write_row(*, sheet: Any, row_index: int, values: tuple[Any, ...], styles: dict[str, Any]) -> None:
    for column_index, value in enumerate(values, start=1):
        cell = sheet.cell(row_index, column_index)
        cell.value = value
        cell.border = styles["cell_border"]
        cell.alignment = styles["wrap_alignment"]


def _apply_sheet_layout(*, sheet: Any, widths: dict[str, float]) -> None:
    for column_name, width in widths.items():
        sheet.column_dimensions[column_name].width = width
    for row_index in range(1, sheet.max_row + 1):
        sheet.row_dimensions[row_index].height = 22


def _styles(*, alignment_cls: Any, border_cls: Any, font_cls: Any, fill_cls: Any, side_cls: Any) -> dict[str, Any]:
    side = side_cls(style="thin", color="C7CED6")
    return {
        "title_font": font_cls(name="Segoe UI", size=16, bold=True),
        "section_font": font_cls(name="Segoe UI", size=12, bold=True),
        "label_font": font_cls(name="Segoe UI", size=10, bold=True),
        "note_font": font_cls(name="Segoe UI", size=10, italic=True, color="5E6B75"),
        "header_font": font_cls(name="Segoe UI", size=10, bold=True, color="FFFFFF"),
        "title_alignment": alignment_cls(horizontal="left", vertical="center"),
        "header_alignment": alignment_cls(horizontal="center", vertical="center", wrap_text=True),
        "wrap_alignment": alignment_cls(horizontal="left", vertical="top", wrap_text=True),
        "header_fill": fill_cls(fill_type="solid", fgColor="2F7A6D"),
        "cell_border": border_cls(left=side, right=side, top=side, bottom=side),
    }


def _placed_rows(result_data: dict[str, Any]) -> list[dict[str, Any]]:
    placed_items = list(result_data.get("placed_items") or [])
    type_totals = _type_totals(result_data=result_data, placed_items=placed_items)
    rows: list[dict[str, Any]] = []
    for index, item in enumerate(_sorted_placed_items(placed_items), start=1):
        position = item.get("position_mm") or {}
        dims = item.get("dimensions_mm") or {}
        item_id = str(item.get("item_id") or "")
        x = _as_number(position.get("x"))
        y = _as_number(position.get("y"))
        z = _as_number(position.get("z"))
        l = _as_number(dims.get("L"))
        w = _as_number(dims.get("W"))
        h = _as_number(dims.get("H"))
        rows.append(
            {
                "place_no": int(item.get("place_no") or index),
                "item_id": item_id,
                "instance_id": str(item.get("instance_id") or ""),
                "name": str(item.get("name") or item_id or "Место"),
                "rotation": str(item.get("rotation") or "XYZ"),
                "source_label": str(item.get("source_path") or "Ручной ящик"),
                "source_kind_label": "Ручной" if item.get("source_kind") == "manual" else "STEP",
                "type_total_quantity": type_totals.get(item_id, 1),
                "x": x,
                "y": y,
                "z": z,
                "l": l,
                "w": w,
                "h": h,
                "dims_text": _dims_text(l, w, h),
                "position_text": _dims_text(x, y, z),
            }
        )
    return rows


def _type_totals(*, result_data: dict[str, Any], placed_items: list[dict[str, Any]]) -> dict[str, int]:
    catalog = result_data.get("catalog") or []
    catalog_totals: dict[str, int] = {}
    if isinstance(catalog, list):
        for item in catalog:
            if not isinstance(item, dict):
                continue
            item_id = str(item.get("item_id") or "")
            quantity = int(item.get("quantity") or 0)
            if item_id and quantity > 0:
                catalog_totals[item_id] = quantity
    if catalog_totals:
        return catalog_totals

    counts = Counter(str(item.get("item_id") or "") for item in placed_items if item.get("item_id"))
    return {item_id: quantity for item_id, quantity in counts.items() if item_id}


def _unplaced_rows(result_data: dict[str, Any]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for item in list(result_data.get("unplaced_items") or []):
        if not isinstance(item, dict):
            continue
        rows.append(
            {
                "name": str(item.get("name") or item.get("item_id") or "Место"),
                "quantity": int(item.get("quantity") or 0),
                "source_label": str(item.get("source_path") or ""),
            }
        )
    return rows


def _sorted_placed_items(placed_items: list[dict[str, Any]]) -> list[dict[str, Any]]:
    return sorted(
        placed_items,
        key=lambda item: (
            _sort_order(item),
            _as_number((item.get("position_mm") or {}).get("x")),
            _as_number((item.get("position_mm") or {}).get("y")),
            _as_number((item.get("position_mm") or {}).get("z")),
            str(item.get("item_id") or ""),
            str(item.get("instance_id") or ""),
        ),
    )


def _sort_order(item: dict[str, Any]) -> int:
    place_no = item.get("place_no")
    if isinstance(place_no, int):
        return place_no
    if isinstance(place_no, float):
        return int(place_no)
    return 10**9


def _truck_dims_text(truck: dict[str, Any]) -> str:
    return _dims_text(
        _as_number(truck.get("length_mm")),
        _as_number(truck.get("width_mm")),
        _as_number(truck.get("height_mm")),
    )


def _used_dims_text(used: dict[str, Any]) -> str:
    return _dims_text(
        _as_number(used.get("L")),
        _as_number(used.get("W")),
        _as_number(used.get("H")),
    )


def _dims_text(first: float, second: float, third: float) -> str:
    return f"{_fmt_number(first)} x {_fmt_number(second)} x {_fmt_number(third)}"


def _fmt_number(value: float) -> str:
    if int(value) == value:
        return str(int(value))
    return f"{value:.3f}".rstrip("0").rstrip(".")


def _as_number(value: Any) -> float:
    if isinstance(value, bool):
        return float(int(value))
    if isinstance(value, (int, float)):
        return float(value)
    return 0.0


def _load_openpyxl() -> tuple[Any, Any, Any, Any, Any]:
    try:
        from openpyxl import Workbook
        from openpyxl.styles import Alignment, Border, Font, PatternFill, Side
    except ImportError as exc:
        raise RuntimeError(
            "openpyxl is required for Excel report export. Install dependencies with 'python -m pip install -e .'"
        ) from exc
    return Workbook, Alignment, Border, Font, PatternFill, Side
